def process_csv(root: Path, csv_path: Path, args: argparse.Namespace) -> tuple[pd.DataFrame, pd.DataFrame]:
    df = read_result_csv(csv_path)

    error_col = choose_col(
        df,
        args.error_col,
        ["board_wrong_best", "board_wrong_mean", "predict_wrong_best", "predict_wrong_mean"],
    )
    loss_col = choose_col(df, args.loss_col, ["loss_min", "loss_mean", "loss_max"])

    run_label = run_label_from_csv(root, csv_path)
    out_dir = csv_path.parent / "plots"
    out_dir.mkdir(parents=True, exist_ok=True)

    summary = summarize_by_side(df, error_col, loss_col)
    summary.insert(0, "run_label", run_label)
    summary.insert(1, "csv_path", str(csv_path.relative_to(root)))

    summary_csv = out_dir / "summary_by_grid_size.csv"
    summary.to_csv(summary_csv, index=False)

    write_latex_summary(
        summary,
        out_dir / "summary_by_grid_size.tex",
        caption=f"Summary by grid size for {run_label}.",
        label="tab:" + "".join(ch if ch.isalnum() else "-" for ch in run_label.lower()).strip("-"),
    )

    # Plots for this CSV.
    save_solve_rate_by_grid_size(
        summary,
        out_dir / "solve_rate_by_grid_size.png",
        title=f"Solve rate by grid size: {run_label}",
    )
    save_sorted_wrong_plot(
        df,
        out_dir / "sorted_wrong_cells.png",
        error_col=error_col,
        title=f"Sorted wrong-cell count: {run_label}",
    )
    save_wrong_distribution_plot(
        df,
        out_dir / "wrong_cells_distribution_by_grid_size.png",
        error_col=error_col,
        title=f"Wrong-cell distribution by grid size: {run_label}",
    )
    save_loss_vs_wrong_plot(
        df,
        out_dir / "loss_vs_wrong_cells.png",
        error_col=error_col,
        loss_col=loss_col,
        title=f"Loss vs. wrong cells: {run_label}",
    )

    print(f"[OK] {csv_path.relative_to(root)}")
    print(f"     plots -> {out_dir.relative_to(root)}")
    solved_df = collect_solved_puzzles(root, csv_path, df, run_label, error_col, loss_col)

    # Also save a per-run solved-puzzle list beside the plots.
    solved_csv = out_dir / "solved_puzzles.csv"
    solved_df.to_csv(solved_csv, index=False)

    print(f"     summary -> {summary_csv.relative_to(root)}")
    print(f"     solved  -> {solved_csv.relative_to(root)} ({len(solved_df)} solved puzzle rows)")

    return summary, solved_df


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Create plots from testing_test_with_csv.py result CSV files."
    )
    parser.add_argument(
        "--root",
        type=Path,
        default=Path(__file__).resolve().parent,
        help="Directory to search recursively. Default: directory containing this script.",
    )
    parser.add_argument(
        "--error-col",
        default=DEFAULT_ERROR_COL,
        help=f"Wrong-cell column to plot. Default: {DEFAULT_ERROR_COL}.",
    )
    parser.add_argument(
        "--loss-col",
        default=DEFAULT_LOSS_COL,
        help=f"Loss column to plot. Default: {DEFAULT_LOSS_COL}.",
    )
    parser.add_argument(
        "--only",
        default=None,
        help="Optional substring filter for CSV paths.",
    )
    args = parser.parse_args()

    root = args.root.resolve()
    csvs = find_result_csvs(root)

    if args.only:
        csvs = [p for p in csvs if args.only in str(p)]

    if not csvs:
        print(f"No result CSVs found below: {root}")
        return

    print(f"Found {len(csvs)} result CSV file(s):")
    for p in csvs:
        print(f"  - {p.relative_to(root)}")
    print()

    all_summaries = []
    all_solved = []
    for csv_path in csvs:
        try:
            summary, solved_df = process_csv(root, csv_path, args)
            all_summaries.append(summary)
            all_solved.append(solved_df)
        except Exception as exc:
            print(f"[ERROR] Could not process {csv_path.relative_to(root)}: {exc}")

    if not all_summaries:
        print("No summaries were generated.")
        return

    combined = pd.concat(all_summaries, ignore_index=True)

    summary_dir = root / "_result_plot_summary"
    summary_dir.mkdir(parents=True, exist_ok=True)

    combined_csv = summary_dir / "combined_summary_by_grid_size.csv"
    combined.to_csv(combined_csv, index=False)

    solved_combined = pd.concat(all_solved, ignore_index=True) if all_solved else pd.DataFrame()
    solved_csv = summary_dir / "solved_puzzles_by_run.csv"
    solved_combined.to_csv(solved_csv, index=False)

    # A compact text version can be convenient for quickly checking puzzle ids.
    solved_txt = summary_dir / "solved_puzzle_indices_by_run.txt"
    with solved_txt.open("w", encoding="utf-8") as f:
        if solved_combined.empty:
            f.write("No solved puzzles found.\n")
        else:
            for run_label, group in solved_combined.groupby("run_label", sort=False):
                f.write(f"{run_label}\n")
                for side, gside in group.groupby("side", sort=True):
                    ids = ", ".join(str(x) for x in gside["puzzle_id"].tolist())
                    f.write(f"  {side}x{side}: {ids}\n")
                f.write("\n")

    # Cross-run comparison plots.
    save_grouped_solve_rate_comparison(
        combined,
        summary_dir / "solve_rate_comparison_by_grid_size.png",
    )

    save_metric_comparison_by_grid_size(
        combined,
        summary_dir / "mean_wrong_comparison_by_grid_size.png",
        metric_col="mean_wrong",
        ylabel="Mean wrong cells",
        title="Mean wrong cells by grid size",
    )

    save_metric_comparison_by_grid_size(
        combined,
        summary_dir / "median_wrong_comparison_by_grid_size.png",
        metric_col="median_wrong",
        ylabel="Median wrong cells",
        title="Median wrong cells by grid size",
    )

    save_metric_comparison_by_grid_size(
        combined,
        summary_dir / "mean_loss_comparison_by_grid_size.png",
        metric_col="mean_loss",
        ylabel="Mean loss",
        title="Mean loss by grid size",
    )

    # Overall plots are now separated, so solve rate and loss are not drawn
    # on the same axes.
    save_overall_bar_comparison(
        combined,
        summary_dir / "overall_solve_rate_comparison.png",
        metric_col="solve_rate",
        ylabel="Solve rate (%)",
        title="Overall solve rate comparison",
        percent=True,
    )

    save_overall_bar_comparison(
        combined,
        summary_dir / "overall_mean_wrong_comparison.png",
        metric_col="mean_wrong",
        ylabel="Mean wrong cells",
        title="Overall mean wrong-cell comparison",
    )

    save_overall_bar_comparison(
        combined,
        summary_dir / "overall_mean_loss_comparison.png",
        metric_col="mean_loss",
        ylabel="Mean loss",
        title="Overall mean loss comparison",
    )

    print()
    print(f"Combined summary -> {combined_csv.relative_to(root)}")
    print(f"Solved puzzles   -> {solved_csv.relative_to(root)}")
    print(f"Solved ids text  -> {solved_txt.relative_to(root)}")
    print(f"Comparison plots -> {summary_dir.relative_to(root)}")


if __name__ == "__main__":
    main()