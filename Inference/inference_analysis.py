#!/usr/bin/env python3
"""
Create result plots from CSV files generated by testing_test_with_csv.py.

Place this file in the same directory as testing_test.py and
testing_test_with_csv.py, then run:

    python plot_test_results.py

The script recursively finds CSV files in subdirectories, computes summary
statistics, and saves plots next to each CSV file in a "plots" folder. It also
creates cross-run comparison plots in "_result_plot_summary".

Expected CSV columns are those written by testing_test_with_csv.py, including:
    side, loss_mean/loss_min, board_wrong_best/board_wrong_mean,
    predict_wrong_best/predict_wrong_mean, puzzle_solved_any_trial

Dependencies:
    pip install pandas numpy matplotlib
"""

from __future__ import annotations

import argparse
import csv
from pathlib import Path
from typing import Iterable

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt


# ---------------------------------------------------------------------
# Configuration defaults
# ---------------------------------------------------------------------

DEFAULT_ERROR_COL = "board_wrong_best"
DEFAULT_LOSS_COL = "loss_min"
DEFAULT_SOLVED_COL = "puzzle_solved_any_trial"

# For very large internal test sets, scatter plots can become heavy.
# The script samples points for visual overlays, but summaries use all rows.
MAX_SCATTER_POINTS_PER_SIDE = 2500
MAX_LOSS_SCATTER_POINTS = 12000

# Avoid re-reading CSV files from output folders.
IGNORE_DIR_NAMES = {"plots", "_result_plot_summary", "__pycache__"}


# ---------------------------------------------------------------------
# File discovery and loading
# ---------------------------------------------------------------------

def find_result_csvs(root: Path) -> list[Path]:
    """Find CSV files below root, excluding generated plot folders."""
    csvs: list[Path] = []
    for p in root.rglob("*.csv"):
        if any(part in IGNORE_DIR_NAMES for part in p.parts):
            continue
        # Keep files that look like testing_test_with_csv output.
        try:
            header = pd.read_csv(p, nrows=0).columns
        except Exception:
            continue
        if {"side", "puzzle_id"}.issubset(set(header)):
            csvs.append(p)
    return sorted(csvs)


def read_result_csv(path: Path) -> pd.DataFrame:
    """Read only the columns needed for plotting/statistics."""
    header = list(pd.read_csv(path, nrows=0).columns)

    wanted = [
        "puzzle_id",
        "variant_id",
        "side",
        "runs",
        "loss_mean",
        "loss_min",
        "loss_max",
        "board_wrong_mean",
        "board_wrong_best",
        "predict_wrong_mean",
        "predict_wrong_best",
        "solved_count",
        "solve_rate",
        "puzzle_solved_any_trial",
        "num_trials_per_puzzle",
        "nca_steps",
        "checkpoint_path",
        "test_stack_dir",
    ]
    usecols = [c for c in wanted if c in header]
    df = pd.read_csv(path, usecols=usecols)

    if "side" not in df.columns:
        raise ValueError(f"{path} does not contain required column 'side'.")

    df["side"] = pd.to_numeric(df["side"], errors="coerce").astype("Int64")
    df = df.dropna(subset=["side"]).copy()
    df["side"] = df["side"].astype(int)

    # Normalize solved column.
    if DEFAULT_SOLVED_COL in df.columns:
        df["solved_bool"] = df[DEFAULT_SOLVED_COL].astype(str).str.lower().isin(
            ["true", "1", "yes"]
        )
    elif "solved_count" in df.columns:
        df["solved_bool"] = pd.to_numeric(df["solved_count"], errors="coerce").fillna(0) > 0
    elif "solve_rate" in df.columns:
        df["solved_bool"] = pd.to_numeric(df["solve_rate"], errors="coerce").fillna(0) > 0
    else:
        raise ValueError(
            f"{path} has no solved column. Expected puzzle_solved_any_trial, solved_count, or solve_rate."
        )

    # Numeric columns.
    for col in [
        "loss_mean",
        "loss_min",
        "loss_max",
        "board_wrong_mean",
        "board_wrong_best",
        "predict_wrong_mean",
        "predict_wrong_best",
        "runs",
        "num_trials_per_puzzle",
        "nca_steps",
    ]:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")

    return df


def choose_col(df: pd.DataFrame, preferred: str, alternatives: Iterable[str]) -> str:
    if preferred in df.columns:
        return preferred
    for alt in alternatives:
        if alt in df.columns:
            return alt
    raise ValueError(f"None of these columns exist: {[preferred, *alternatives]}")


def run_label_from_csv(root: Path, csv_path: Path) -> str:
    """Make a compact label from the CSV location."""
    rel = csv_path.relative_to(root)
    parts = rel.parts

    # Prefer top-level result folder name.
    if len(parts) >= 2:
        label = parts[0]
        # Include one nested run name if useful, e.g. clip_tighter.
        if "checkpoints" in parts:
            idx = parts.index("checkpoints")
            if idx + 1 < len(parts) - 1 and parts[idx + 1] != csv_path.stem:
                # Usually this is a checkpoint or run subfolder. Use only if not step_XXXX.
                candidate = parts[idx + 1]
                if not candidate.startswith("step_") and candidate != csv_path.name:
                    label += f" / {candidate}"
        return label
    return csv_path.stem


