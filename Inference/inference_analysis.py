def summarize_by_side(df: pd.DataFrame, error_col: str, loss_col: str) -> pd.DataFrame:
    rows = []
    for side, group in sorted(df.groupby("side"), key=lambda x: x[0]):
        n = len(group)
        solved = int(group["solved_bool"].sum())
        row = {
            "grid_size": f"{side}x{side}",
            "side": int(side),
            "puzzles": int(n),
            "solved": solved,
            "solve_rate": solved / n if n else np.nan,
            "mean_wrong": float(group[error_col].mean()) if error_col in group else np.nan,
            "median_wrong": float(group[error_col].median()) if error_col in group else np.nan,
            "min_wrong": float(group[error_col].min()) if error_col in group else np.nan,
            "max_wrong": float(group[error_col].max()) if error_col in group else np.nan,
            "mean_loss": float(group[loss_col].mean()) if loss_col in group else np.nan,
            "median_loss": float(group[loss_col].median()) if loss_col in group else np.nan,
        }
        rows.append(row)

    n_total = len(df)
    solved_total = int(df["solved_bool"].sum())
    rows.append(
        {
            "grid_size": "Total",
            "side": -1,
            "puzzles": int(n_total),
            "solved": solved_total,
            "solve_rate": solved_total / n_total if n_total else np.nan,
            "mean_wrong": float(df[error_col].mean()) if error_col in df else np.nan,
            "median_wrong": float(df[error_col].median()) if error_col in df else np.nan,
            "min_wrong": float(df[error_col].min()) if error_col in df else np.nan,
            "max_wrong": float(df[error_col].max()) if error_col in df else np.nan,
            "mean_loss": float(df[loss_col].mean()) if loss_col in df else np.nan,
            "median_loss": float(df[loss_col].median()) if loss_col in df else np.nan,
        }
    )

    return pd.DataFrame(rows)


def write_latex_summary(summary: pd.DataFrame, out_path: Path, caption: str, label: str) -> None:
    """Write a compact LaTeX table. Optional, but useful for Overleaf."""
    lines = []
    lines.append(r"\begin{table}[H]")
    lines.append(r"\centering")
    lines.append(rf"\caption{{{caption}}}")
    lines.append(rf"\label{{{label}}}")
    lines.append(r"\begin{tabular}{@{}l r r r r r@{}}")
    lines.append(r"\toprule")
    lines.append(
        r"\textbf{Grid size} & \textbf{Puzzles} & \textbf{Solved} & "
        r"\textbf{Solve rate} & \textbf{Mean wrong} & \textbf{Median wrong} \\"
    )
    lines.append(r"\midrule")

    for _, row in summary.iterrows():
        grid = row["grid_size"]
        puzzles = int(row["puzzles"])
        solved = int(row["solved"])
        rate = row["solve_rate"] * 100
        mean_wrong = row["mean_wrong"]
        median_wrong = row["median_wrong"]

        if grid == "Total":
            lines.append(r"\midrule")
            grid_tex = r"\textbf{Total}"
            puzzles_tex = rf"\textbf{{{puzzles}}}"
            solved_tex = rf"\textbf{{{solved}}}"
            rate_tex = rf"\textbf{{{rate:.1f}\%}}"
            mean_tex = rf"\textbf{{{mean_wrong:.2f}}}"
            med_tex = rf"\textbf{{{median_wrong:.2f}}}"
        else:
            grid_tex = f"${grid.replace('x', r' \\times ')}$"
            puzzles_tex = str(puzzles)
            solved_tex = str(solved)
            rate_tex = f"{rate:.1f}\\%"
            mean_tex = f"{mean_wrong:.2f}"
            med_tex = f"{median_wrong:.2f}"

        lines.append(
            f"{grid_tex} & {puzzles_tex} & {solved_tex} & "
            f"{rate_tex} & {mean_tex} & {med_tex} \\\\"
        )

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append(r"\end{table}")

    out_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


